-- TABUNGAN QURBAN
CREATE TABLE T_TABUNGAN_QURBAN (
  ID_TABUNGAN    INT AUTO_INCREMENT PRIMARY KEY,
  ID_PELANGGAN   INT NOT NULL,
  NAMA_TABUNGAN  VARCHAR(150),
  JENIS_TARGET   ENUM('SAPI','KAMBING','DOMBA') NOT NULL DEFAULT 'KAMBING',
  TARGET_NOMINAL DECIMAL(15,2) NOT NULL DEFAULT 0,
  ID_HEWAN       INT NULL,
  ID_PESANAN     INT NULL,
  TERKUMPUL      DECIMAL(15,2) NOT NULL DEFAULT 0,
  STATUS         CHAR(1) NOT NULL DEFAULT 'A',
  TGL_BUAT       DATETIME NOT NULL DEFAULT NOW(),
  CONSTRAINT FK_TABUNGAN_PELANGGAN
    FOREIGN KEY (ID_PELANGGAN) REFERENCES T_PELANGGAN(ID_PELANGGAN)
    ON UPDATE CASCADE,
  CONSTRAINT FK_TABUNGAN_HEWAN
    FOREIGN KEY (ID_HEWAN) REFERENCES T_HEWAN(ID_HEWAN)
    ON DELETE SET NULL ON UPDATE CASCADE,
  CONSTRAINT FK_TABUNGAN_PESANAN
    FOREIGN KEY (ID_PESANAN) REFERENCES T_PESANAN(ID_PESANAN)
    ON DELETE SET NULL ON UPDATE CASCADE
);

-- SETORAN
-- Satu setoran boleh terkait ke tabungan qurban ATAU cicilan pesanan qurban.
CREATE TABLE T_SETORAN (
  ID_SETORAN   INT AUTO_INCREMENT PRIMARY KEY,
  ID_TABUNGAN  INT NULL,
  ID_PESANAN   INT NULL,
  NOMINAL      DECIMAL(15,2) NOT NULL,
  METODE       VARCHAR(30) NOT NULL DEFAULT 'TUNAI',
  STATUS       VARCHAR(30) NOT NULL DEFAULT 'LUNAS',
  REF_BAYAR    VARCHAR(100),
  TGL_SETOR    DATETIME NOT NULL DEFAULT NOW(),
  CONSTRAINT FK_SETORAN_TABUNGAN
    FOREIGN KEY (ID_TABUNGAN) REFERENCES T_TABUNGAN_QURBAN(ID_TABUNGAN)
    ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT FK_SETORAN_PESANAN
    FOREIGN KEY (ID_PESANAN) REFERENCES T_PESANAN(ID_PESANAN)
    ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT CK_SETORAN_TARGET
    CHECK (
      (ID_TABUNGAN IS NOT NULL AND ID_PESANAN IS NULL)
      OR (ID_TABUNGAN IS NULL AND ID_PESANAN IS NOT NULL)
    )
);

