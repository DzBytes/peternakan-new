-- PELANGGAN
CREATE TABLE T_PELANGGAN (
  ID_PELANGGAN     INT AUTO_INCREMENT PRIMARY KEY,
  NAMA_LENGKAP     VARCHAR(150) NOT NULL,
  EMAIL            VARCHAR(150) NOT NULL UNIQUE,
  PASSWORD_HASH    VARCHAR(255) NOT NULL,
  NO_HP            VARCHAR(20),
  ROLE             ENUM('PELANGGAN','ADMIN') NOT NULL DEFAULT 'PELANGGAN',
  STATUS           CHAR(1)     NOT NULL DEFAULT 'P',
  EMAIL_VERIFIED   TINYINT(1)  NOT NULL DEFAULT 0,
  VERIFY_TOKEN     VARCHAR(100),
  VERIFY_EXPIRES   DATETIME,
  RESET_TOKEN      VARCHAR(100),
  RESET_EXPIRES    DATETIME,
  TGL_DAFTAR       DATETIME    NOT NULL DEFAULT NOW()
);

-- HEWAN / STOK
CREATE TABLE T_HEWAN (
  ID_HEWAN   INT AUTO_INCREMENT PRIMARY KEY,
  JENIS      ENUM('SAPI','KAMBING','DOMBA') NOT NULL,
  NAMA       VARCHAR(150) NOT NULL,
  BERAT_INFO VARCHAR(50),
  HARGA      DECIMAL(15,2) NOT NULL,
  STOK       INT NOT NULL DEFAULT 0,
  GRADE      VARCHAR(5),
  SHARE      INT NOT NULL DEFAULT 1,
  KATEGORI   VARCHAR(30),
  STATUS     CHAR(1) NOT NULL DEFAULT 'A'
);

CREATE VIEW V_STOK_HEWAN AS
  SELECT *
  FROM T_HEWAN
  WHERE STOK > 0 AND STATUS = 'A';

-- PESANAN
CREATE TABLE T_PESANAN (
  ID_PESANAN      INT AUTO_INCREMENT PRIMARY KEY,
  ID_PELANGGAN    INT NOT NULL,
  JENIS_LAYANAN   ENUM('QURBAN','AQIQAH') NOT NULL,
  TGL_PESAN       DATETIME NOT NULL DEFAULT NOW(),
  TGL_PELAKSANAAN DATE,
  ALAMAT_KIRIM    TEXT,
  STATUS          VARCHAR(30) NOT NULL DEFAULT 'Menunggu Konfirmasi',
  TOTAL           DECIMAL(15,2) NOT NULL DEFAULT 0,
  METODE_BAYAR    VARCHAR(30),
  CONSTRAINT FK_PESANAN_PELANGGAN
    FOREIGN KEY (ID_PELANGGAN) REFERENCES T_PELANGGAN(ID_PELANGGAN)
    ON UPDATE CASCADE
);

CREATE TABLE T_DETAIL_PESANAN (
  ID_DETAIL    INT AUTO_INCREMENT PRIMARY KEY,
  ID_PESANAN   INT NOT NULL,
  ID_HEWAN     INT NOT NULL,
  JUMLAH       INT NOT NULL DEFAULT 1,
  HARGA_SATUAN DECIMAL(15,2) NOT NULL DEFAULT 0,
  IS_PATUNGAN  CHAR(1) NOT NULL DEFAULT 'N',
  CONSTRAINT FK_DETAIL_PESANAN
    FOREIGN KEY (ID_PESANAN) REFERENCES T_PESANAN(ID_PESANAN)
    ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT FK_DETAIL_HEWAN
    FOREIGN KEY (ID_HEWAN) REFERENCES T_HEWAN(ID_HEWAN)
    ON UPDATE CASCADE
);

CREATE TABLE T_JADWAL_PEMOTONGAN (
  ID_JADWAL INT AUTO_INCREMENT PRIMARY KEY,
  TANGGAL   DATE NOT NULL,
  TEMPAT    VARCHAR(200) NOT NULL,
  KUOTA     INT NOT NULL DEFAULT 0,
  STATUS    CHAR(1) NOT NULL DEFAULT 'A'
